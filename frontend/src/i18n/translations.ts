const getValue = (tree: TranslationTree, key: string): TranslationLeaf | TranslationTree | undefined => {
  return key.split('.').reduce<TranslationLeaf | TranslationTree | undefined>((current, part) => {
    if (!current || typeof current === 'string' || Array.isArray(current)) {
      return undefined;
    }
    return current[part];
  }, tree);
};

export const resolveSupportedLocale = (locale: string | null | undefined): SupportedLocale => {
  const normalized = locale?.toLowerCase().split(/[-_]/)[0] as SupportedLocale | undefined;
  return SUPPORTED_LANGUAGES.find((entry) => entry.code === normalized)?.code ?? DEFAULT_LOCALE;
};

export const getDeviceLocale = (): SupportedLocale => {
  try {
    const locale = Intl.DateTimeFormat().resolvedOptions().locale;
    return resolveSupportedLocale(locale);
  } catch {
    return DEFAULT_LOCALE;
  }
};

export const translate = (
  locale: SupportedLocale,
  key: string,
  params?: Record<string, string | number>,
): string => {
  const value = getValue(translations[locale], key) ?? getValue(translations[DEFAULT_LOCALE], key);

  if (typeof value !== 'string') {
    return key;
  }

  if (!params) {
    return value;
  }

  return Object.entries(params).reduce((result, [paramKey, paramValue]) => {
    return result.replace(new RegExp(`{{${paramKey}}}`, 'g'), String(paramValue));
  }, value);
};

export const translateList = (locale: SupportedLocale, key: string): string[] => {
  const value = getValue(translations[locale], key) ?? getValue(translations[DEFAULT_LOCALE], key);
  return Array.isArray(value) ? value : [];
};